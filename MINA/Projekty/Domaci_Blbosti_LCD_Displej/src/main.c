#include "mina_shield_mbed.h"
#include "mbed_shield_lcd.h"
#include "nucleo_usart.h"

volatile uint32_t ticks = 0;
void SysTick_Handler(void)
{
	ticks++;
}

int main(void)
{

    uint32_t timeConstant = 100;

	SystemCoreClockUpdate();

	SysTick_Config(SystemCoreClock / 1000);

	Usart2Initialization(38400);


	////////// LCD ///////////////////////////////////////////////////////////////////////////////////////////////////////////////

	if(!MBED_LCD_init())
	{
		while(1);
	}

    MBED_LCD_InitVideoRam(0x00);      // fill content with 0 = clear memory buffer

	////////// LCD ///////////////////////////////////////////////////////////////////////////////////////////////////////////////


	uint8_t x = 0;

			while (1) {

				if (ticks >= timeConstant)
				{

					timeConstant = ticks + 100;

					MBED_LCD_DrawLine(x,0,x,31,true);

					MBED_LCD_DrawLine(x-32,0,x-32,31,false);

					x++;

				    MBED_LCD_VideoRam2LCD();         // move changes in video buffer to LCD


				}



		}
	}
