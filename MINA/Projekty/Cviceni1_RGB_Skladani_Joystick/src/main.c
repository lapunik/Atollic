{
tickElapsed = true;
}

int main(void)
{

	GPIOConfigurePin(RGBLED_BLUE, ioPortOutputPushPull);
	GPIOConfigurePin(RGBLED_RED, ioPortOutputPushPull);
	GPIOConfigurePin(RGBLED_GREEN, ioPortOutputPushPull);

	GPIOConfigurePin(JOY_CENTER,ioPortInputFloat);
	GPIOConfigurePin(JOY_DOWN,ioPortInputFloat);
	GPIOConfigurePin(JOY_LEFT,ioPortInputFloat);
	GPIOConfigurePin(JOY_UP,ioPortInputFloat);
	GPIOConfigurePin(JOY_RIGHT,ioPortInputFloat);


	// pokud se ptám jestli je na vstupu jednièka nebo nula IDR, moje funkce mi to neumí øíct

	SystemCoreClockUpdate();

	SysTick_Config(SystemCoreClock); // nastavení èasovaèe pro pøerušení

	GPIOWrite(RGBLED_BLUE,true);
	GPIOWrite(RGBLED_GREEN,true);
	GPIOWrite(RGBLED_RED,true);

  while (1)
  {

		if(GPIORead(JOY_UP))
		{

			GPIOToggle(RGBLED_BLUE);

			while(GPIORead(JOY_UP));

		}
		if(GPIORead(JOY_DOWN))
		{

if((!GPIORead(RGBLED_BLUE))&&(!GPIORead(RGBLED_GREEN))&&(!GPIORead(RGBLED_RED)))
{
	GPIOWrite(RGBLED_BLUE,true);
	GPIOWrite(RGBLED_GREEN,true);
	GPIOWrite(RGBLED_RED,true);
}else
{
	GPIOWrite(RGBLED_BLUE,false);
	GPIOWrite(RGBLED_GREEN,false);
	GPIOWrite(RGBLED_RED,false);
}

        while(GPIORead(JOY_DOWN));

		}
		if(GPIORead(JOY_LEFT))
		{

			GPIOToggle(RGBLED_GREEN);

			while(GPIORead(JOY_LEFT));

		}
		if(GPIORead(JOY_RIGHT))
		{

			GPIOToggle(RGBLED_RED);

			while(GPIORead(JOY_RIGHT));

		}
		if(GPIORead(JOY_CENTER))
		{

			GPIOWrite(RGBLED_BLUE,true);
			GPIOWrite(RGBLED_GREEN,true);
			GPIOWrite(RGBLED_RED,true);

			while(GPIORead(JOY_CENTER));

		}

  }

}
